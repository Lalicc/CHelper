//TRANSPARENT NAVBAR
$(window).scroll(function () {
  if ($(window).scrollTop() >= 50) {
    $('.navbar').addClass('js-navbar-bg')
    $('.navbar').removeClass('js-transparent')
  } else {
    $('.navbar').removeClass('js-navbar-bg')
    $('.navbar').addClass('js-transparent')
  }
})
